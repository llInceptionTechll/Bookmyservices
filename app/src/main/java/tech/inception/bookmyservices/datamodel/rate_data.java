public class rate_data {

    String id , item , price ;

    public void setId(String id) {
        this.id = id;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public String getItem() {
        return item;
    }

    public String getPrice() {
        return price;
    }

}
